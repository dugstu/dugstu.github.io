        <h5>DELIVERY</h5>
        <p>From there we moved into delivery and quickly started building software. We ran a triple-track agile model where we were doing discovery, delivery, and usability testing all running in parallel.
        We also ended up providing a fair amount of executive advisory and marketing support throughout our sprints &mdash; helping Authority Brands tell the story of why they invested in Successware's transformation, and also guiding them toward future innovation.</p>
        <h4>SIDEBAR: A HITCH</h4>
        <p>With just six weeks left in the development of our first major release, Successware's largest competitor bought the 3rd-party mobile application provider our client depended upon and aggressively pursued Successware's customers to convert. We partnered with our client to quickly evaluate the landscape and recommended adjusting their short and medium-term product roadmap, both in feature delivery and timeline.</p>
        <p>This shift entailed focusing on particular parts of the technician's job that couldn't be done by the offices, and also laid the groundwork for future functionality. Just enough to give customers confidence that they would be able to continue business, and could see how new features would improve their capabilities and work efficiency as time went on.</p>
        <p>Our team worked round the clock to make this new product strategy happen in time. With the mobile app's delivery success, our account grew into transforming the whole Successware Platform which is still in development today.</p>
