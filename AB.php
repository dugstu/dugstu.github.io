        <h5>INCEPTION</h5>
        <p>After that initial research, we conducted our inception, joined by our full team who traveled from China, spending time with the client in Maryland and New York. For two weeks, with about 30 people in the room, we defined the project context, vision, users, key stakeholders, as-is workflow, pain points, to-be journey, technical stack, and legacy software considerations.</p>
        <p>This appears to be really expensive work to get this many high salaries together for many days, but it pays off when you've clarified goals, set direction, and cleared up assumptions on an organizational scale.</p>
