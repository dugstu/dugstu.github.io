        <h5>ONSITE RESEARCH</h5>
        <p>To better understand the technicians, their customers (namely homeowners), franchise owners, and the home services business in general, our design and product team headed out on a summer-long road trip.  We visited businesses all over the United States who used Successware as well as the product's main competitors. We spent time working alongside the technicians, while they were on-site, to better understand how they worked. The learnings we gathered from this greatly informed and drove our roadmap.</p>
