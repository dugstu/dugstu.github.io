        <p>This was a successful endeavor. After we released version 1, market share went up and Authority Brands has been able to retire their third-party application, saving hefty licensing fees, and ensuring that their customers wouldn't be forced to move to Authority's competitors. Additionally, this success led to TW expanding our relationship with Authority to be responsible for transforming their entire platform. Finally, we're releasing new versions on a monthly cadence. For a client like Authority, where they were used to releasing once every 5-7 years, this is a significant improvement.</p>
