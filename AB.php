        <h3>Authority Brands</h3>
        <p>Authority Brands is the second largest franchisor of home repair brands in the US. They have a portfolio of companies that cover HVAC, plumbing, electrical work, and more. If you need work done on your home, chances are good that you've hired an Authority Brands franchise.</p>
        <p>Authority owns a software suite called Successware. It was responsible for every aspect of franchise business: accounting, reporting, dispatch, technician management, sales, inventory management, etc. Their system was old and missing numerous capabilities that come with modern web-based technologies. They engaged with Thoughtworks to update this software suite.</p>
        <p>We decided that a good starting place was a mobile app for field technicians to interface with their home offices.</p>
        <h4>PEOPLE | GOALS | CONSTRAINTS</h4>
        <p>I oversaw a team of four designers, three product analysts, a dozen developers, and two QAs. Most of the team was located in China, with a pair of designers in the US responsible for usability testing. Our primary goal was the construction of a mobile app for phones and tablets that would allow technicians to receive jobs, take notes on those jobs, and change the status of those jobs. Addtionally, technicians would be able to provide product and service purchase options to customers directly from the app. This necessitated an app that would be "front-facing." This meant something that would provide technicians with the maximum amount of information necessary to quickly understand a job, while also providing customer-facing pages that were clear, easy to understand, and appealling.</p>
        <p>For the most part, this was a fairly straightforward project. Initially our only constraints were reasonable time and budget limits. However, late in the project we came across a hurdle. (See below.)</p>
        <h4>PROCESS</h4>
        <p>The process here consisted of three major parts.</p>
