        <h5>RESEARCH</h5>
        <p>Research went in two directions: competitive research and internal interviews. We looked at what NCL offered, as well as how they fared in the competitive landscape. We considered reviews, youtube videos of various cruises, online travel forums. We reserved cruises (as best we could) on competitors’ lines. </p>
        <p>We also interviewed a number of NCL employees, around 70 people in total. This included VPs of various domains (hotel, dining, etc.), ship captains, heads of housekeeping, cruise directors, head of security. It’s always a good idea to get a sense of how things work from people all up and down the chain. Additionally you gain a lot of experience in a short time by talking with long-time employees.</p>

