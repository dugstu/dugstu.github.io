        <h3>NCLH</h3>
        <p><p>Norwegian Cruise Line Holdings is the third-largest cruise operator in the world. It operates three separate cruise lines, with different price points and clientele. During the pandemic, all of their cruise ships were shut down; they wisely determined that was an optimal time to update their digital presence.</p>
        <p>In autumn of 2020, they engaged with Thoughtworks to build a mobile app to be used by guests onboard their cruises. Thoughtworks convinced them that it would be a better use of resources to consider the totality of the guest experience, not only the time onboard. (We expected that, if the experience onboard was good enough, it would mean people spending less time staring at their phones.) They agreed, and we built an engagement to design a superior guest experience.</p>

        <h4>PEOPLE | GOALS | CONSTRAINTS</h4>
        <p> I oversaw a team of three designers, two product analysts, two technical analysts, and a subject matter expert. We had eight weeks to define a customer experience vision and a strategic direction for potential innovation. To do this we would map out current and future CX experiences. Within those maps we would highlight touchpoints and their related business outcomes.</p>
        <p>There were a couple of unusual constraints, primarily around not being able to work in-person with anyone due to the pandemic. Historically, we like to work directly with people. Further, normally we would want to tour the physical spaces where we would expect potential tools would be used. In this case, that would be the ships and the ports. The pandemic made this impossible. Instead we had to use photos, plans, found videos of the spaces.</p>
        <p>Additionally, cruise experiences are a combination of a number of different domains: transportation, security, retail, casinos, dining, leisure, hotels, cruise ships have everything these days. (One of their ships has an outboard go-kart track on the top of it!)</p>
        <p>As a result, we had to be particularly intentional with our work to ensure we considered all of these various elements, while simultaneously avoided spending too much time going down rabbit holes.</p>

        <h4>PROCESS</h4>
        <p>The process included six parts, from mapping NCL's current state to potential destinations for their CX experience.</p>

