        <h5>IDEATION</h5>
        <p>From those maps, we are able to highlight pain points and ideas for how to improve particular interactions, but we’re also synthesizing ideas into a more cohesive overall experience. We went through a number of exercises to think about a different, holistic way to approach guest experiences.</p>
        <p>And from those exercises we came up with a number of different ideas that we were able to put into various story forms, including some very early prototypes.</p>
