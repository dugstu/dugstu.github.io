        <p>From there we were able to build a plan for digital products and tools to make this happen, to treat all of these ideas holistically. This was particularly important: one of the primary messages we stressed was that the guest experience was the totality of the thing: we couldn’t consider these products separately just yet. We had to understand how they would all work in concert.</p>

        <h4>RESULTS</h4>
        <p>This process was a success. We were able to take much of NCL's leadership on the journey of not only what to do, but how we got there, together. They had all played a part in figuring this out, and they could demonstrate each of the steps we took to create the plan. So we were able to cut down on the noise, on the decisions made by HIPPOs without any justification, and that made it easier for everyone to understand the vision and get onboard. Our contacts at NCL were able to take this plan to their leaders and begin steps to implement this plan.</p>
        <p>This work led directly to multiple multi-month projects engaging three dozen consultants on three different continents working over the past eighteen months. Our work with NCL continues to thrive, and is one of our largest customer experience success stories.</p>

