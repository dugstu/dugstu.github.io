        <h5>GUEST INTERVIEWS</h5>
        <p>While talking with NCL employees, we also interviewed around 40 guests. This included people who had been on Norwegian cruises as well as a number who had been on competitors’ lines. These interviews allowed us to understand their opinions about motivations for cruising, as well as favorite activities both on the ship and in the ports. Plus, we were able to understand how they felt during every step of their first and last days of their cruise.</p>
        <p>This work started to pay off immediately. Early on we were able to dispel some assumptions on the part of our client. For instance, there was an opinion on the part of many NCL employees that the first day of a cruise is the worst: there are a lot of lines inside ugly warehouses with fluorescent lights. Security takes a long time. Everyone is required to muster once they’re on the ship, to learn safety protocols, it’s unclear when guests will get their luggage, and on and on.</p>
        <p>However, after we started talking with guests, we found that there were very few complaints about this. Certainly, this part of the experience provides opportunities for improvement, but from the guests’ perspective, this isn’t a big deal, mostly because of the excitement of the beginning of the trip. You're near this massive ship, larger than you can imagine, and you’re slowly getting closer to boarding. It creates anticipation. And once you’re onboard, there are so many things to see and do, and the excitement of the upcoming ports. It turns out that there’s so much excitement that it mutes the pain of most serious hassles on the first day.</p>
        <p>But the last day? TONS of guest complaints about that one. For one thing, your trip is coming to an end. You have to face the inevitable fact that you are going to have to go back to the real world, your lousy job, whatever problems you normally face. Additionally, you get hit with a bill that is almost always considerably larger than you expected. And finally, once the ship docks, the crew doesn’t hesitate to get guests off swiftly, because they have to clean and prep the entire ship to take on another batch of guests that same day. So there were a lot of opportunities there.</p>
        <p>We took standardized sets of notes for each guest and looked for patterns. This data fed the next steps.</p>
        
