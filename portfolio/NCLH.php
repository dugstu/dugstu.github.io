        <h5>ARCHETYPES</h5>
        <p>We discovered patterns around how people cruise, their motivations and goals. The overall distinctions focused on whether the guest was primarily on the cruise for the port destinations, the people with whom they would interact, or the experience of being on the open sea.</p>
        <p>From there we were able to slot each person into an archetype and then map out what was important in each of those areas. There were some nice revelations around how those three points of interest interacted with each other.</p>

