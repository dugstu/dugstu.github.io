        <h5>VALUE CANVAS</h5>
        <p>We took all of these ideas, packaged them as stories, and ran them by leaders from several parts of the organization. We ran some workshops to determine the value of each idea from the perspective of the guest experience, the business value, and the technical complexity. Ideas that had high CX value, provided the potential for a big lift to the business, and were simpler technically, got prioritized as things to try first.</p>
