        <p>A few years ago I started putting this model into practice, using a lot of the techniques mentioned above, and determined where the pain points are in the designers' journeys: both within client projects, but also their overall careers. That led to the formation and oversight of a number of working groups within the design practice. I'm a firm believer that improvement comes from within, so one of the things that I implemented was getting every designer we have involved in at least one of these working groups. We've focused energies on hiring, career paths, tool sets, improving evangelization of our work, and more.</p>
