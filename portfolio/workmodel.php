        <p>This is the kind of work that often gets sidelined because it requires effort but it's harder to draw a bright line to the payoff. It's not client work, so it's hard to see where the profit is. But it's there, because it improves the capabilities of individual designers, and when you scale that you get exponential improvements. And that's a win-win situation.</p>
