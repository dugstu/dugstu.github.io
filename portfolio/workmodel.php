        <h2>A model for cultivating good work</h2>
        <p>In addition to my work with clients, I have been a leader within the product and design practices at Thoughtworks. From my time working with designers of all stripes &mdash; people from all over the world, new and long-experienced, people who came straight out of a D-school vs people for whom design is a second or even third career &mdash; I have built out a model of how designers work, and how to improve their lives to get the best work possible out of them.</p>
        <p>The model looks like this.</p>
        <figure>
            <a href="#model-craft" class="thumb">
                <img src="../img/portfolio/model-craft.png" alt="Work Model - Craft">
