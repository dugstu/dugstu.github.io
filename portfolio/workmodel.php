        <p>Next is community. This is all about discussing the work. This requires other people in your field with whom you can discuss problems you're having, feedback on your work, career paths, ideas on how to improve any area of your job. If craft is what is required to be a designer, community is how you become a good designer: when you're able to get input from people with similar experiences, and who you can trust to have a body of knowledge that makes them worth hearing, you're not working in a vacuum.</p>
        <figure>
            <a href="#model-capability" class="thumb">
                <img src="../img/portfolio/model-capability.png" alt="Work Model - Capability">
            </a>
            <div class="lightbox" id="model-capability">
                <a href="#_" class="btn-close">&times;</a>
                    <img src="../img/portfolio/model-capability.png">
            </div>
        </figure>
        <p>Finally there's capability. This is how you can be a successful designer: by having impact. This is where knowledge peeks over into wisdom, seeing the forest for the trees. These are the efforts we make to ensure that our work sees the light of day, and is useful and usable, and solves the right problems for our clients and the right problems for their customers.</p>
        <p>I've been a strong advocate here for using the methodologies of design not only for products, but for ways of working, our relationships with our clients. Design at Thoughtworks didn't used to be considered a capability, but that has changed, and I'm proud of that.</p>
        <figure>
