        <p>The first layer is craft. That's all about doing the work: it's the tools and techniques that you use day-in day-out. In the case of product design, this is what allows you to do research, interaction design, visual design. Being able to practice your craft makes you a designer. Every designer coming to into a practice has to be able to show their craft before getting hired, and every designer should continue to hone their skills over time.</p>
        <figure>
            <a href="#model-community" class="thumb">
                <img src="../img/portfolio/model-community.png" alt="Work Model - Community">
            </a>
            <div class="lightbox" id="model-community">
                <a href="#_" class="btn-close">&times;</a>
                    <img src="../img/portfolio/model-community.png">
            </div>
