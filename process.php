        <h2>HOW DO YOU MAKE A GOOD PRODUCT?</h2>
        <p>Digital products have three primary drivers: customer needs, business value, and technical capabilities. If customers have a problem, and you can build a solution to that problem, and that solution makes you money, you have a good product.</p>
        <p>I’m a lead designer at ThoughtWorks, a consulting firm that specializes in agile software delivery. The nature of our work means our design practice is a little different than agencies. We work mostly with older, larger clients: enterprise companies with decades of technological and physical baggage.</p>
        <p>Many enterprise companies know they need to compete in ways unlike the ways to which they’re accustomed. Other players have entered the game and are threatening their whole way of operating (cf. Amazon). These companies often look to outside firms for help. That’s not a bad idea. But the way they engage with these firms often causes its own host of problems.</p>
        <p>First they hire a firm to help them with their strategy. They engage with Accenture or McKinsey or the like, who do a good job assessing their client and the market and trends to determine a Course of Action, a General Business Strategy, etc. The consultants help their client to lay out a problem, and then they work very hard to come up with a general solution, and then they collect their money and move on.</p>
        <p>Then the enterprise company hires a design firm, an IDEO or Frog or what-have-you to bring clarity to that solution. This design firm makes very nice decks laying out a visual language, and design systems, and prototypes and an obscene number of wireframes. If they’ve really done their homework they have done some usability testing with some of these early prototypes as well.</p>
        <p>Then the company hires a development shop like ThoughtWorks to build it. And the TW team spends some time understanding the problem for which they’re solving and the solution that has been designed. And usually, there are some problems: priorities have changed and the designed solution doesn’t account for those changes, or the solution as designed will cost more than something smaller and simpler in the short- to medium-run.</p>
