        <p>That’s why we advocate for a cross-functional team of decision-makers at all stages of the process: from visioning all the way to feature creation. This team has representatives from the business, the customers (via design), and engineering. By insisting on this cross-functional team, there is a natural tension that comes from the needs and constraints of each side, that leads to a solid, better product.</p>
