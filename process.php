        <p>And when our shop looks for guidance as to the reasoning behind particular elements of the strategy or the design, they often find that pivotal decision makers have left, and there are holes in current knowledge as to why certain choices were made. These chasms of ignorance cause slowdowns or worse, result in the wrong product being built, and highlights the primary problem that this kind of process causes: <strong>it focuses on an output instead of an outcome.</strong></p>
        <p>Instead of trying to improve the user experience to increase engagement with the company in an effort to build a longer-term relationship with customers, the goal is to build an app. Never mind if that app is something that people will use, or will make money for the business. Getting the app built is the goal. This is not a good goal. The app is a tool. The tool is only a good product if its use profits the business and if people use it. </p>
