        <p>But part of our job as designers is to get experienced as quickly and painlessly as possible. So how do we do this?</p>
        <p>I have some techniques that I use that I put under “experience scaffolding.” The idea here is that we’re supposed to get a sense of the experience that the client has without building out all the details, much like if you put up the scaffolding of a building you get a sense of the size of the building, without actually building it.</p>
