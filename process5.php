        <h2>How do you make a good product?</h2>
        <h3>Part 5: Synthesis</h3>
        <p>Let’s take a moment to consider the magic that is synthesis. Synthesis is my favorite part, because it’s where clarity is produced. It’s also the toughest part, in that it’s the hardest thing to measure and takes the most work to justify. It’s wiggly and has plenty of risk, and so it takes hard work and intentionality to produce something with confidence.</p>
        <p>The purpose of synthesis is to take all of the data gathered during the research process and transform it into actionable knowledge. There are some steps to get there.</p>
        <p>There’s a model of cognizance that breaks down stuff people can understand into four groups: data, information, knowledge, and wisdom.</p>
