        <aside>Knowledge: ???</aside>
        <p>Going from information to knowledge is a little different. First, we should understand what exactly knowledge is. Many people smarter than me have tackled this:</p>
        <blockquote>Knowledge transforms “information into instructions.”<br><small>Ackoff, <i>International Society for General Systems Research</i></small></blockquote>
        <blockquote>Knowledge is like the recipe that lets you make bread out of the information-ingredients of flour and yeast (with data as the atoms of the ingredients).<br><small>Zeleny</small></blockquote>
        <blockquote>Knowledge is the combination of data and information, to which is added expert opinion, skills and experience, to result in a valuable asset which can be used to aid decision making.<br><small>European Committee for Standardization, <i>Guide to Good Practice in Knowledge Management</i></small></blockquote>
        <p>But my favorite definition comes from Plato:</p>
        <aside>Knowledge is the set of beliefs that are true and we are justified in believing.</aside>
        <p>How do we know we’re justified in believing something? Generally, that’s from experience. So generally, my definition is:</p>
        <aside>Knowledge: Information + Experience</aside>
        <p>So then what is experience?</p>
        <p>AN experience refers to a time period of things happening with a beginning and end. You are having an experience right now. An experience is something that happens to you, in all of your wonderful complexity and that is also affected by other people and natural events and forces and other designed artifacts. And former experiences affect and change you in all your wonderful complexity, which affects your future experiences in turn.</p>
