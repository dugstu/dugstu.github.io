        <aside>Wisdom: Prioritized knowledge</aside>
        <p>In this context, I define wisdom as prioritized knowledge. It’s hard to get to this point, but when you do, when you can look at the process you’ve adopted and lay out why you’ve gone down this road and not another, it provides confidence that you will be able to build a product that is useful and worth the time of your customers. And that’s a good place to be.</p>
