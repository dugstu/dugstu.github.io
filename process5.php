        <p>Experience refers to the constant stream of inputs, outputs, processing, awareness, and “self talk” that occurs during conscious life. People who spend a lot of time on a job get a lot of experience. It’s hard to transfer this experience to people. Anyone who has onboarded onto a project with a team that’s been there a while knows what I’m talking about here.</p>
