        <p>So when we do these things, we start to get a sense of these people’s experience. And when we do that, we start to appreciate them in a new way. Maybe before we thought they were just lazy or inefficient, and afterward we see them in a new light. And so now we empathize with them. And that’s how we get from knowledge to wisdom.</p>
