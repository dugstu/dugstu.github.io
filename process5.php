        <aside>Data: Facts/Statistics</aside>
        <p>This model starts with data. Data is easily understood. Any facts or statistics, without any context, is data.</p>
        <aside>Information: Organized Data</aside>
        <p>Information is simply organized data. Any time we line up data in a table or a diagram of some kind, or prioritize data points, we create information.</p>
        <p>So creating information is simply making meaning out of that data. That can come in the form of data tables, maps, or process/workflow diagrams, among others. The point is to find some way of grounding your data in some form of organization to provide priority and the potential for insights.</p>
        <figure>
