        <p>The alternative is to build the concrete and steel structure with guardrails from the get-go, but in the time it takes to complete 10% of that bridge, we could have the rope bridge up and running. So now we’ve already made a whole, good thing. And then we can see what the people who are not yet crossing the bridge would need to be able to cross the bridge, and solve for those people next. So now we can upgrade the bridge with the most important features to get another batch of people across the water, and hopefully finish that in short order as well.</p>
        <p>The point here is that at every stage, the product is a complete one. There is no point building an app where the first two-thirds of the task are pixel-perfect if the last third is non-existent. That doesn’t actually help anyone. Better to build a rope bridge that people can use, and THEN start upgrading.</p>
