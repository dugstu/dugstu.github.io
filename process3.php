        <h2>How do you make a good product?</h2>
        <h3>Part 3: What’s actually important about agile software delivery</h3>
        <p>So when you’ve decided on an initiative, how do you build a good product? The most common approach is agile delivery. There is an absurd amount written about agile software delivery. I am not going to get into that too much except to say that one of its primary goals is to make quality software quickly.</p>
        <p>The question then becomes, what does “quality” mean? For me, that means that the software helps users to complete an important task or tasks, as simply and easily as possible. “Complete” is the key word here. Whenever I design products, we take the single most vital task and build it so that at least one user could <em>DO</em> a task to completion. So for instance, to continue my metaphor from earlier, if we have to get people from and island to the mainland via a bridge, first we’d build a small rope bridge.</p>
